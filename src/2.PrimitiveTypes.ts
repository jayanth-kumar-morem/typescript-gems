// TypeScript allows you to define variables with specific data types.
// This helps in catching type-related errors during compile time.

// BOOLEAN TYPE:
// The boolean data type can only have two values: true or false.
let isPresent: boolean = true; // Declares a variable named 'isPresent' and assigns it the value 'true'.

// NUMBER TYPE:
// The number data type is used for numeric values.
let magicNumber: number = 42; // Declares a variable named 'magicNumber' and assigns it the value '42'.

// STRING TYPE:
// The string data type is used for textual data.
let myName: string = 'John'; // Declares a variable named 'myName' and assigns it the value 'John'.

// UNDEFINED TYPE:
// The undefined data type has only one value: undefined.
// It represents the absence of a value or a computed value.
let notDefined: undefined = undefined; // Declares a variable named 'notDefined' and assigns it the value 'undefined'.

// NULL TYPE:
// The null data type has only one value: null.
// It represents the intentional absence of any value or object.
let wontBeDefined: null = null; // Declares a variable named 'wontBeDefined' and assigns it the value 'null'.

// SYMBOL TYPE:
// The symbol data type is a unique and immutable data type.
// It is often used to create unique object property keys.
let penta: symbol = Symbol('penta'); // Declares a variable named 'penta' and assigns it a unique symbol value.

// BIGINT TYPE:
// The bigint data type is used to represent whole numbers larger than 2^53 - 1.
let bigInt: bigint = 100n; // Declares a variable named 'bigInt' and assigns it the value '100' as a bigint.
